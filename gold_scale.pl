    print "##########\n";
    print (1.61803399 ** $i);
    print "\n";
    print ((1.61803399 ** $i) + (1.61803399 ** ($i-4)));
    print "\n";
    print ((1.61803399 ** $i) + (1.61803399 ** ($i-3)));
    print "\n";
    print ((1.61803399 ** $i) + (1.61803399 ** ($i-3)) + (1.61803399 ** ($i-5)));
    print "\n";
    print ((1.61803399 ** $i) + (1.61803399 ** ($i-2)));
    print "\n";
    print ((1.61803399 ** $i) + (1.61803399 ** ($i-2)) + (1.61803399 ** ($i-5)));
    print "\n";
    print ((1.61803399 ** $i) + (1.61803399 ** ($i-2)) + (1.61803399 ** ($i-4)));
    print "\n";
    print ((1.61803399 ** $i) + (1.61803399 ** ($i-2)) + (1.61803399 ** ($i-4)) + (1.61803399 ** ($i-6)));
    print "\n";
}

